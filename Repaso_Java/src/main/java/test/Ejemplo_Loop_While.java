package test;

public class Ejemplo_Loop_While {

	public static void main(String[] args) {

		int a = 1;
		
		//SALIR DEL LOOP CUANDO "a" SEA MAYOR QUE "4"
		while (a<=4) 
		{
			System.out.println("El valor de A:" + a); //EL SIGNO "+" NOS SIRVE PARA CONCATENAR
			
			a++; //INCREMENTAMOS EL VALOR  
		}

	}

}
