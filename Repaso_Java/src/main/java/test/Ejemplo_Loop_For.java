package test;
public class Ejemplo_Loop_For {

	public static void main(String[] args) {
		
		int y = 5;
		
		/*
		 * BUCLE COMIENZA CUANDO X ES IGUAL A "0"
		 * X ES UNA VARIABLE DENTRO DEL BUCLE FOR
		 * CORRE HASTA QUE <=5 */
		
		for (int x=0; x <= y; x++) 
		{
			System.out.println ("Valor de X es:" + x);
		}
	}
}
