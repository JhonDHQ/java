package test;

public class Ejemplo_If_Else_If 
{

	public static void main(String[] args) 
	{
		int i=1;  //CREAMOS UNA VARIABLE ENTERO
		
		if(i==10) 
			{
				System.out.println("I es igual a 10");
			}
		else if(i==15) 
			{
				System.out.println("i es igual a 15");
			}
		else if(i==20) 
			{
				System.out.println("i es igual a 20");
			}
		else 
			{
				System.out.println("i no es igual a ninguna de las condiciones");
			}
	}

}
